//  Chapter 05 - final variables

void main() {
  //  - final type
  //  the final types variable does not change, that mean the final variable can only be set once.
  final productNumber = '225dazD2000A';

  //  impossible, final type is cannot be change
  productNumber = '256dbbA3000Z';

  // the final types variable can have a type, but that is not duty
  final int serialNumber = 55301663;
  final String userEmail = 'abcd@gmail.com';
}
