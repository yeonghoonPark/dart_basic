// Chapter 01 - basic data types

void main() {
  // - data types
  // all data types is class.

  // - String
  String fruit = 'apple';

  // - boolean
  bool isSave = true;

  // - integer, 정수
  int one = 1;

  // - double, 실수
  double two_point_five = 2.5;

  // - num, 정수 or 실수
  // num can be both integer and double
  num number = 5;
  number = 5.5;
}
